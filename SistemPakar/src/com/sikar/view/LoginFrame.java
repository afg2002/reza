    public boolean LoginValidation(String user, String pass){
        boolean hasil = false;
        try {
            o = OrangDAO.loadByUsername(user);
            if(user.equals(o.getUsername())&&pass.equals(o.getPassword( ))){
                hasil =true;
            }else{
                hasil = false;
            }
        } catch (SQLException e) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, e);
        }
        return hasil;
    }

