    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (txtUsername.getText().length()==0){
            JOptionPane.showMessageDialog(null, "ISI USERNAME TERLEBIH DAHULU");
        } else if (txtPassword.getText().length()==0){
            JOptionPane.showMessageDialog(null, "ISI PASSWORD TERLEBIH DAHULU");
        } else if (LoginValidation(txtUsername.getText(), txtPassword.getText())) {
            txtUsername.setText("");
            txtPassword.setText("");
            if(o.getLevel().equals("Admin")) {
                JOptionPane.showMessageDialog(null, "Selamat Datang Admin");
                this.dispose();
                AdminFrame f = new AdminFrame();
                f.txtNamaAdmin.setText(o.getNama());
                f.setVisible(true);
            } 
        } else {
            JOptionPane.showMessageDialog(null, "Gagal, cek kembali username atau password anda!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

