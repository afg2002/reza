    public void tableFill() {
    List<HasilJoin> hasilJoins = recHasilDAOs;  // Assuming recHasilDAOs is a List
    int dataLength = hasilJoins.size();

    // Prepare data for the table model
    Object[][] data = new Object[dataLength][5];  // Only include necessary columns
    for (int i = 0; i < dataLength; i++) {
      HasilJoin hasilJoin = hasilJoins.get(i);
      data[i][0] = i + 1;  // Start index at 1 for "No" column
      data[i][1] = hasilJoin.getId_user();
      data[i][2] = hasilJoin.getNama();
      data[i][3] = hasilJoin.getNama_kecerdasan();
      data[i][4] = hasilJoin.getKecocokan();
    }

    // Define table headers
    String[] judul = {"No", "ID Pengguna", "Nama", "Kecerdasan", "Kecocokan"};

    // Set the table model
    tabelCiriMinatBakat.setModel(new DefaultTableModel(data, judul));
}


    

