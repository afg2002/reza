    private void updateTable(List<HasilJoin> hasilJoins) {
  // Prepare data for the table model
  Object[][] data = new Object[hasilJoins.size()][5];
  for (int i = 0; i < data.length; i++) {
    HasilJoin hasilJoin = hasilJoins.get(i);
    data[i][0] = i + 1;
    data[i][1] = hasilJoin.getId_user();
    data[i][2] = hasilJoin.getNama();
    data[i][3] = hasilJoin.getNama_kecerdasan();
    data[i][4] = hasilJoin.getKecocokan();
  }

  // Define table headers
  String[] judul = {"No", "ID Pengguna", "Nama", "Kecerdasan", "Kecocokan"};

  // Create a DefaultTableModel
  DefaultTableModel tableModel = new DefaultTableModel(data, judul);

  // Set the table model for the JTable
  tabelCiriMinatBakat.setModel(tableModel);

  // Optionally: Clear previous selection
  tabelCiriMinatBakat.getSelectionModel().clearSelection();
}

